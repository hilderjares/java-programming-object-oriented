package br.quixada.ufc.si.poo.model;

public class Calculadora {
	public static int soma(int valor_um, int valor_dois) {
		return valor_um + valor_dois;
	}
	
	public static int soma(int valor_um, int valor_dois, int valor_tres) {
		return valor_um + valor_dois + valor_tres;
	}
	
	public static float soma(float valor_um, float valor_dois) {
		return valor_um + valor_dois;
	}
}
